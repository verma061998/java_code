package Day2;

public class Positive {

	public static void main(String[] args) {
		int a = -0;
		if(a<0)
		{
			System.out.println(a+" is Negative integer");
		}
		else if(a==0)
		{
			System.out.println(a+"  is Zero");
		}
		else {
			System.out.println(a+"  is Positive integer");
		}

	}

}
